# Solved:
# (M) Number of Islands
# https://leetcode.com/problems/number-of-islands/

from collections import deque
from typing import List


class Solution:
    def _processIsland(self, grid: List[List[str]], x: int, y: int):
        xmax, ymax = len(grid) - 1, len(grid[0]) - 1
        que = deque([(x, y)])
        grid[x][y] = "0"
        while que:
            x, y = que.popleft()
            if x > 0 and grid[x - 1][y] == "1":
                grid[x - 1][y] = "0"
                que.append((x - 1, y))
            if y > 0 and grid[x][y - 1] == "1":
                que.append((x, y - 1))
                grid[x][y - 1] = "0"
            if x < xmax and grid[x + 1][y] == "1":
                que.append((x + 1, y))
                grid[x + 1][y] = "0"
            if y < ymax and grid[x][y + 1] == "1":
                que.append((x, y + 1))
                grid[x][y + 1] = "0"

    def numIslands(self, grid: List[List[str]]) -> int:
        res = 0
        for x, row in enumerate(grid):
            for y in range(0, len(row)):
                if grid[x][y] == "1":
                    self._processIsland(grid, x, y)
                    res += 1

        return res


